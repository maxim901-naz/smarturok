def _load_env_file():
    """
    Load .env from project root or app root.
    Uses python-dotenv when available, with a lightweight fallback parser.
    """
    env_candidates = (BASE_DIR.parent / '.env', BASE_DIR / '.env')
    env_path = next((p for p in env_candidates if p.exists()), None)
    if not env_path:
        return

    try:
        from dotenv import load_dotenv  # type: ignore
    except Exception:
        load_dotenv = None

    if load_dotenv:
        load_dotenv(env_path, override=False)
        return

    for raw_line in env_path.read_text(encoding='utf-8').splitlines():
        line = raw_line.strip()
        if not line or line.startswith('#'):
            continue
        if line.startswith('export '):
            line = line[7:].strip()
        if '=' not in line:
            continue
        key, value = line.split('=', 1)
        key = key.strip()
        value = value.strip()
        if len(value) >= 2 and (
            (value[0] == '"' and value[-1] == '"')
            or (value[0] == "'" and value[-1] == "'")
        ):
            value = value[1:-1]
        os.environ.setdefault(key, value)


def env_first(*names, default=''):
    for name in names:
        value = os.getenv(name)
        if value is not None and value != '':
            return value
    return default


def _load_secret_from_file(path_value):
    if not path_value:
        return ''

    secret_path = Path(path_value)
    if not secret_path.is_absolute():
        repo_path = BASE_DIR.parent / secret_path
        app_path = BASE_DIR / secret_path
        if repo_path.exists():
            secret_path = repo_path
        elif app_path.exists():
            secret_path = app_path

    try:
        return secret_path.read_text(encoding='utf-8').strip()
    except OSError:
        return ''


