def env_list(name, default=''):
    value = os.getenv(name, default)
    return [item.strip() for item in value.split(',') if item.strip()]


def env_int(name, default):
    value = os.getenv(name)
    if value is None or value == '':
        return default
    try:
        return int(value)
    except (TypeError, ValueError):
        return default


def env_bool(name, default=False):
    value = os.getenv(name)
    if value is None:
        return default
    return value.strip().lower() in {'1', 'true', 'yes', 'on'}


def _build_sqlite_db(path_value):
    path = path_value.strip() if path_value else ''
    if not path:
        return {'ENGINE': 'django.db.backends.sqlite3', 'NAME': BASE_DIR / 'db.sqlite3'}
    db_path = Path(path)
    if not db_path.is_absolute():
        db_path = BASE_DIR / db_path
    return {'ENGINE': 'django.db.backends.sqlite3', 'NAME': db_path}


def _database_from_url(db_url):
    if not db_url:
        return None

    parsed = urlparse(db_url)
    scheme = (parsed.scheme or '').lower()

    if scheme in {'postgres', 'postgresql', 'pgsql'}:
        return {
            'ENGINE': 'django.db.backends.postgresql',
            'NAME': unquote((parsed.path or '/').lstrip('/')),
            'USER': unquote(parsed.username or ''),
            'PASSWORD': unquote(parsed.password or ''),
            'HOST': parsed.hostname or '127.0.0.1',
            'PORT': str(parsed.port or 5432),
            'CONN_MAX_AGE': env_int('DJANGO_DB_CONN_MAX_AGE', 60),
            'OPTIONS': {
                'sslmode': env_first('DJANGO_DB_SSLMODE', default='prefer'),
            },
        }

    if scheme in {'sqlite', 'sqlite3'}:
        sqlite_path = unquote(parsed.path or '')
        if parsed.netloc:
            sqlite_path = f"//{parsed.netloc}{sqlite_path}"
        if sqlite_path in {'', '/'}:
            return _build_sqlite_db('')
        if sqlite_path.startswith('/'):
            return {'ENGINE': 'django.db.backends.sqlite3', 'NAME': sqlite_path}
        return _build_sqlite_db(sqlite_path)

    return None


